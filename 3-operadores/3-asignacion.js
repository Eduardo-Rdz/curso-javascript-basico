// asignacion basica operador =
var x = 5;
console.log("1:", x);

// actualizar el valor de x usando su mismo valor
// se resuelven primero los operadores aritmeticos
x = x + 1;
console.log("2:", x);

// asignacion +=
x += 2; // <= x = x + 2;
console.log("3:", x);

// asignacion -=
x -= 2; // <= x = x - 2;
console.log("4:", x);

// asignacion *=
x *= 2; // <= x = x * 2;
console.log("5:", x);

// asignacion /=
x /= 2; // <= x = x / 2;
console.log("6:", x);

// asignacion %=
x %= 2; // <= x = x % 2;
console.log("7:", x);
